  ¿Listo para ser un baterista virtual? Toca la batería online, elige tu juego de batería favorito y juega con el teclado.<br>
Virtual Drumming es una colección de juegos de batería gratis en línea donde puedes tocar ritmos de batería junto a tu música favorita, la batería virtual se puede tocar con el teclado del ordenador eligiendo entre los sets y los sonidos de muchos grandes bateristas rock, heavy metal y jazz. La batería virtual es un simple instrumento de percusión que ayuda a conocer los elementos de la batería acústica y su función. Usted puede encontrar grandes set de batería con muchos platillos y doble bombo, tambores de jazz esenciales o conjuntos de rock. Toca en tiempo real pulsando las teclas que corresponden a los elementos de la batería virtual (caja, toms, platillos, bombo y hihat), eligiendo el set de batería que prefieres. La correspondencia de las teclas es visualizada y modificada pulsando el botón "keys". Los sonidos de los juegos de batería virtual y sus teclas son diferentes en cada set. Para los sets de la batería virtual y los juegos de batería online se necesita JavaScript.
