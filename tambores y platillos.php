<h2>

Los tambores utilizados en el set de batería
</h2>

<p>

<strong>El redoblante o caja:</strong> Es un tambor membranófono que cuenta con unos bordones en el parche resonante, lo cual le proporciona un sonido brillante y estridente característico. Al redoblante también se le conoce como tarola y es parte fundamental de la batería.
<br>
<br>
<strong>El bombo:</strong> Es un tambor cuyo tamaño puede variar entre las 18” y las 26” de diámetro y las 14” y 22” de profundidad. Debido a su gran tamaño proporciona un sonido grave, ejecutado a través de un pedal que el percusionista maneja con el pie. En algunos casos, se maneja con pedal doble, utilizando ambos pies.
<br>
<br>
<strong>Los toms:</strong> Generalmente se utilizan uno o dos, y se soportan sobre el bombo, aunque en algunos casos no es así, sobre todo cuando se utilizan más de dos. Los toms pueden variar en su tamaño, desde 6” hasta 15 de diámetro.
<br>
<br>
<strong>Tom de piso o granadero:</strong> Algunas veces tienen soportes independientes, o pueden depender del pedestal de algún platillo. En una batería puede haber hasta dos toms de piso, uno con la afinación más grave que el otro, variando también en su tamaño desde 142 hasta 20”.
</p>

