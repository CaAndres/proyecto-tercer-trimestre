<h2>

Los platillos utilizados en la batería
</h2>

<strong>Crash:</strong> Es un platillo o címbalo que se utiliza para dar énfasis durante los ritmos. Un crash pude ser de 12” o hasta 22”.
<br>
<br>
<strong>Hi-hats:</strong> Es un sistema compuesto por dos platillos que instalados en un soporte con pedal, hace el movimiento de uno contra el otro, produzca el sonido. También se pueden tocar abiertos o cerrados, dependiendo de la intención del baterista o el tipo de música ejecutada. Comúnmente, los hi-hats suelen ser de 14” o 15”, pero tambien se han fabricado de 16”. Charles, Charleston o contratiempos, son otro de los nombres que se les puede dar al Hi-hats.
<br>
<br>
<strong>Ride:</strong> Es un platillo grande de entre 17” y 32” que tiene como función llevar el ritmo en sustitución del Hi-hats, dependiendo de la intención del baterista y la cadencia que quiera otorgarle a las canciones.
<br>
<br>
<strong>Splash: </strong>Es un platillo de menor tamaño de entre 5” y 13”, el cual es utilizado para aportar ciertos efectos y para dar acento en canciones de poca intensidad.
<br>
<br>
<strong>China:</strong> Es un platillo que se utiliza para efectos especiales, son colocados al revés y pueden variar su tamaños entre las 10” hasta 22”.
<br>
<br>
<strong>Crash-ride:</strong> Se utilizan como crash y también como ride, debido a su sonido variable en color y potencia.
<br>
<br>
La batería se toca con baquetas o escobillas, las cuales pueden variar mucho en su forma, peso, tamaño, color y material. Nuevamente, cabe destacar que todas esas características variables, dependen la intencionalidad y los gustos del músico. Por lo cual en la música rock y sus variantes, es común el uso de baquetas pesadas, debido a la potencia que proporcionan.
<br>
<br>
En el caso de las escobillas, estas poseen en su extremo unas cerdas que pueden ser de material plástico o metálico, estas últimas generan un sonido un poco más brillante y con utilizadas con mayor frecuencia en el Blues o el Jazz.
<br>
<br>
En cuanto a los pedales, estos se emplean para controlar algunos elementos de la batería, como pueden ser los Hi-hats, el bombo, y en algunos pocos casos, para tambores. Los pedales para el bombo se componen de:
<br>
<br>

<ul>
<li>Una plataforma sobre la cual, el pie aplica fuerza.
<li>Un mazo pesado que golpea el parche del bombo, sus extremo puede tener una superficie de materiales variables entre el plástico, la madera o el fieltro.
<li>Un sistema de tracción que provoque la transmisión del movimiento y la tensión necesaria para que el mazo del pedal vuelva a su posición de reposo.
<li>En años recientes, el uso del pedal doble se ha hecho más común, pues este sistema permite al baterista tocar patrones más complejos sin tener que utilizar un segundo bombo.
</ul>
<br>
<br>
En cuanto al Hi-hat, el funcionamiento es parecido al del pedal del bombo, aunque su función es la de hacer que el platillo superior golpee al inferior, abriéndolos o cerrándolos.
</p>

