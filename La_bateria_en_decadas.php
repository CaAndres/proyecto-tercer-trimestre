<p>
En la década de los 40, aparecen más bateristas para marcar un periodo, como es el caso de Sid Catlett, en quien se puede apreciar una técnica muy versátil para ejecutar solos, sin embargo, aún se mantienen muchos de los rudimentos y las marchas similares a las militares en las canciones. Con la llegada del baterista conocido como el “Papa” Jo Jones, cambia un poco esa tendencia y le proporciona mas melodía a los sonidos de la batería.
<BR>
<BR>
Es importante destacar, que ya para los años 50 se introducen los parches fabricados con materiales sintéticos, con la marca Remo y el fabricante Rogers, específicamente en el año 1959, crea un sistema de soportes, con los que los toms, quedarían fijados sobre el bombo.
</p>

