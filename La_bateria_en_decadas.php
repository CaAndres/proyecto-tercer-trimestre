<p>
Con el boom del rock’n’roll, la llegada de grupos musicales como The Beatles y Rolling Stone, la batería se vuelve un instrumento muy popular entre los jóvenes de la época. Los fabricantes de baterías, comienzan a implementar materiales diferentes llegando a producir baterías con un sonido de alta calidad. La década de los 60, trae consigo la aparición de muchos talentosos bateristas entre los cuales se pueden destacar Keith moon, de la banda Th Who y más tarde John Bonham junto a Led Zeppelin (en este artículo hablamos sobre ellos, pincha aquí)
</p>

 




