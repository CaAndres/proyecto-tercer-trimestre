<p>
dedicada a la construcción artesanal y restauración de Snares, bombos, toms, Sets completos de baterías bajo los requerimientos y exigencias de los bateristas profesionales en cuanto a sonido, ROCAFER a podido abrir una nueva ventana al futuro dentro de la industria Nacional no solo por la excelencia y profesionalidad en el trabajo sino por la exclusividad en la elección de la materia prima en maderas exóticas en las zonas tropicales del Ecuador y también maderas importadas desde Argentina y Canadá. La fusión entre la Caoba (Mahogany), Tilo, Nogal, Oak, Maple hace que el producto sea de primer Nivel.​
<br>
<br>

Una de las preguntas más comunes entre los bateristas del ECUADOR es que si es posible que en este País se construyan Baterias Profesionales?.​
<br>
<br>
Nosotros podemos decir que SI, con la misma calidad y garantía de las grandes empresas, contamos con asesoramiento técnico a nivel internacional, solo es cuestión que los músicos empiecen a creer en el trabajo Nacional, entendemos que han habido muchas decepciones  cuando vemos guitarras nacionales y el músico no le gusta ya sea por sonido o acabado pobre o pequeñas cosas de percusión y damos razón porque a veces hay personas que piensan que hacer un instrumento es como hacer un mueble de casa, se necesita saber mucho de acústica y tener un acabado de primera. A cada uno de nuestros trabajadores se le exige mucho en el acabado final que forma parte de la estética en cualquier producto de primera.
</p>

