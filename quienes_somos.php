   	<h2>¿QUIENES SOMOS?</h2>
   </center>

   <P>
Es una empresa pequeña. Realmente, no es ni siquiera una empresa, es más bien una idea. La idea de que cada uno puede tener su propia caja, su propio sonido. Una caja realmente artesanal, hecha a tu medida y totalmente única. PDAL Drums soy solo yo, y por suerte un montón de gente que me apoya: familia y amigos.
   </P>
   <br>
   <p>
Varus Tamburi Italiani nace en Septiembre 2008 en Bologna (Italia) por parte de Paolo Palmieri.
<br>
Paolo empezó en esa época a dar luz a sus inspiraciones y sus ideas, con taladros, tornillos y cascos dio vida a sus primeras creaciones. En el 2013 se incorpora en el proyecto Thomas Mantovani que vive en Valencia (Spain)
<br>
Entre los dos enseguida se nota el gran feeling y en poco tiempo llegan a conseguir baterías de nivel más que satisfactorio que no tienen nada que envidiar a las grandes marcas americanas y asiáticas, con el objetivo de proponer en el mercado un producto de alta gama pero con un precio más asequible, y empezar a mover el “Italian Style”
<br>
<br>
El objetivo de Varus drums, no tener limites, asesorando al baterista para que pueda realizar la batería de sus sueños con la mejor calidad posible.
<br>
Los cascos de fabricación Italiana son de madera seleccionada y de altísima calidad, cuidando todo en detalle, controlados uno a uno. Brindamos también la posibilidad de elegir entre la fabricación en capas o en “Stave”
<br>
Los modelos estándar vienen fabricados en capas de Arce, 5,4 mm para los toms y 7,2 mm en los bombos. Para otros tipos de madera consúltanos.
   </p>

